export class MathService {
  /**
   * Permet de sommer deux entier
   * @param x
   * @param y
   */
  additioner(x: number, y: number): number {
    return x + y;
  }

  /**
   * Permet de soustraire deux entier
   * @param x
   * @param y
   */
  soustraire(x: number, y: number): number {
    return x - y;
  }
}
